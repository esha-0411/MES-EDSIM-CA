ORG 0000H

MOV R0, 50H       ; A
MOV R1, 51H       ; B

CHECK:
MOV A, R0
JZ A_ZERO
MOV A, R1
JZ B_ZERO

DEC R0
DEC R1
SJMP CHECK

A_ZERO:
MOV A, R1
JZ EQUAL
MOV 52H, #0FFH    ; A < B
SJMP END

B_ZERO:
MOV A, R0
JZ EQUAL
MOV 52H, #01H     ; A > B
SJMP END

EQUAL:
MOV 52H, #00H     ; A = B

END:
SJMP END
END

